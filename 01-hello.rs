// Double slashes denote comments

// Rust is a general-purpose programming language
// with a focus on performance while ensuring
// memory safety without using a garbage collector,
// which is a form of automatic memory management

// The following examples focus more on the interesting features
// of rust to hopefully help you decide whether you'd like to consider
// learning more about rust
fn main() {
    // Use `println` to print to the standard output
    // `println` is a macro, NOT a function, so it requires a `!`
    println!("Hello, world!");

    // Variables are created with the `let` keyword
    let x = 12;

    // The `{}` is substituted by the given argument
    println!("x is {}", x);
}
